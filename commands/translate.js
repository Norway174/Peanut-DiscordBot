const Discord = require("discord.js");
const translate = require('google-translate-api');
//const langs = require("../util/langs.json")

exports.run = (client, message, args) => {
	
	translate(args.join(" "), {to: 'en'}).then(res => {
		
		let lang = res.from.language.iso
		
		//Here, we build the emblem for the online server.
				const embed = new Discord.RichEmbed()
					//.setTitle("Translate")
					.setColor(0x4D90FE)
					//.setDescription( res.text )
					.setFooter( "Translated for " + message.author.username + "." )
					.addField("Detected: " + langs[lang], args.join(" "), true)
					.addBlankField(true)
					.addField("English", res.text, true)
					//.setThumbnail("http://i.imgur.com/2JUhMfW.png")
					//.setTimestamp()
				
				//And then edit the first message we sent. We don't want duplicate messages in our chat.
				message.channel.send({embed});
		
		//console.log(res.text);
		//=> I speak English
		//console.log(langs[lang]);
		//=> nl
	}).catch(err => {
		console.error(err);
	});
	
	//message.delete(5);

};

exports.conf = {
  enabled: true,
  guildOnly: false,
  aliases: ['t'],
  permLevel: 1
};

exports.help = {
  name: 'translate',
  description: 'Translates the given text to English.',
  usage: 'translate <any text>'
};


